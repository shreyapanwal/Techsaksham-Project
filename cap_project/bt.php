            <h1>1896</h1> <br>
            <p>
                The 1896 Summer Olympics (Greek: Θερινοί Ολυμπιακοί Αγώνες 1896, romanized: Therinoí Olympiakoí Agónes 1896), officially known as the Games of the I Olympiad (Greek: Αγώνες της 1ης Ολυμπιάδας, romanized: Agónes tis 1is Olympiádas) and commonly known as Athens 1896 (Greek: Αθήνα 1896), was the first international Olympic Games held in modern history. Organised by the International Olympic Committee (IOC), which had been created by French aristocrat Pierre de Coubertin, it was held in Athens, Greece, from 6 to 15 April 1896.
                Fourteen nations (according to the IOC, though the number is subject to interpretation) and 241 athletes (all males; this number is also disputed) took part in the games.[5][4] Participants were all European, or living in Europe, with the exception of the United States team. Over 65% of the competing athletes were Greek. Winners were given a silver medal, while runners-up received a copper medal. Retroactively, the IOC has converted these to gold and silver, and awarded bronze medals to third placed athletes. Ten of the 14 participating nations earned medals. The United States won the most gold medals, 11, while host nation Greece won the most medals overall, 47. The highlight for the Greeks was the marathon victory by their compatriot Spyridon Louis. The most successful competitor was German wrestler and gymnast Carl Schuhmann, who won four events.

Athens had been unanimously chosen to stage the inaugural modern Games during a congress organised by Coubertin in Paris on 23 June 1894, during which the IOC was also created, because Greece was the birthplace of the Ancient Olympic Games. The main venue was the Panathenaic Stadium, where athletics and wrestling took place; other venues included the Neo Phaliron Velodrome for cycling, and the Zappeion for fencing. The opening ceremony was held in the Panathenaic Stadium on 6 April, during which most of the competing athletes were aligned on the infield, grouped by nation. After a speech by the president of the organising committee, Crown Prince Constantine, his father officially opened the Games. Afterwards, nine bands and 150 choir singers performed an Olympic Hymn, composed by Spyridon Samaras, with words by poet Kostis Palamas.

The 1896 Olympics were regarded as a great success. The Games had the largest international participation of any sporting event to that date. The Panathenaic Stadium overflowed with the largest crowd ever to watch a sporting event.[6] After the Games, Coubertin and the IOC were petitioned by several prominent figures, including Greece's King George and some of the American competitors in Athens, to hold all the following Games in Athens. However, the 1900 Summer Olympics were already planned for Paris and, except for the Intercalated Games of 1906, the Olympics did not return to Greece until the 2004 Summer Olympics, 108 years later.
